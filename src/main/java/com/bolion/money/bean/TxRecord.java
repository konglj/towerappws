package com.bolion.money.bean;

import java.util.List;

public class TxRecord {
	
	private int id;
	
	private String txid;
	
	private int fee;
	
	private String adddate;
	
	private String completedate;
	
	private int state;

	private String towername;
	
	private String towertypeimg;
	
	List<TxRecordTower> RecordDetails; 
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTxid() {
		return txid;
	}

	public void setTxid(String txid) {
		this.txid = txid;
	}

	public int getFee() {
		return fee;
	}

	public void setFee(int fee) {
		this.fee = fee;
	}

	public String getAdddate() {
		return adddate;
	}

	public void setAdddate(String adddate) {
		this.adddate = adddate;
	}

	public String getCompletedate() {
		return completedate;
	}

	public void setCompletedate(String completedate) {
		this.completedate = completedate;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public String getTowername() {
		return towername;
	}

	public void setTowername(String towername) {
		this.towername = towername;
	}

	public String getTowertypeimg() {
		return towertypeimg;
	}

	public void setTowertypeimg(String towertypeimg) {
		this.towertypeimg = towertypeimg;
	}

	public List<TxRecordTower> getRecordDetails() {
		return RecordDetails;
	}

	public void setRecordDetails(List<TxRecordTower> recordDetails) {
		RecordDetails = recordDetails;
	}
}
