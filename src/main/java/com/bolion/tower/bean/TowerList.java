package com.bolion.tower.bean;

import java.util.ArrayList;
import java.util.List;

public class TowerList {
	
	private long gstime;
	
	private int towerpages;
	
	private List<Tower> towers=new ArrayList<Tower>();

	public long getGstime() {
		return gstime;
	}

	public void setGstime(long gstime) {
		this.gstime = gstime;
	}

	public int getTowerpages() {
		return towerpages;
	}

	public void setTowerpages(int towerpages) {
		this.towerpages = towerpages;
	}

	public List<Tower> getTowers() {
		return towers;
	}

	public void setTowers(List<Tower> towers) {
		this.towers = towers;
	}
	
	

}
